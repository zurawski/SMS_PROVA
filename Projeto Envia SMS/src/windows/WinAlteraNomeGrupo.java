    private void fillComboGrupos()
    {
        SMSDAO dao = new SMSDAO();
        ArrayList<String> lista = new ArrayList<String>();
        lista = dao.fillComboGrupos();
        for (int i = 0; i < lista.size(); i++)
        {
            jGrupos.addItem(lista.get(i));
        }
        
    }
