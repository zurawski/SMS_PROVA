    public boolean setAcessoLogin(String usuario, String senha)
    {
        SMSDAO dao = new SMSDAO();
        boolean findUsuario = dao.findUsuario(usuario, senha);
        
        if (findUsuario == true)
        {
            return true;
        }
        else
        {
            return false;
        } 
    }
             

